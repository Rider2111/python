# this is a simple program to check arithmetic operators in python
x = int(input("\nEnter first number: "))
y = int(input("Enter second number(other than zero): "))

print(x ,'+' , y ,'is', x+y)
print(x ,'-' , y ,'is', x-y)
print(x ,'*' , y ,'is', x*y)
print(x ,'/' , y ,'is', x/y)
print(x ,'%' , y ,'is', x%y)
print(x ,'//' , y ,'is', x//y)
print(x ,'**' , y ,'is', x**y)